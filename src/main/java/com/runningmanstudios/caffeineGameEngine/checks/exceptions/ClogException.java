package com.runningmanstudios.caffeineGameEngine.checks.exceptions;

public class ClogException extends Exception {

    public ClogException(String message) {
        super(message);
    }

    public ClogException(Throwable cause) {
        super(cause);
    }

    public ClogException(String message, Throwable cause) {
        super(message, cause);
    }

}
